import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.dirname(fileURLToPath(import.meta.url));
const includesRoot = path.join(root, "includes");
const branchFactor = 5;
const includeDepth = 5;
const targetLines = 2_000;
const implicitGlobalMode = process.env.ASP_LSP_BENCH_IMPLICIT_GLOBALS === "1";

function ensureDirectories() {
  fs.mkdirSync(includesRoot, { recursive: true });
  for (let depth = 1; depth <= includeDepth; depth += 1) {
    fs.mkdirSync(path.join(includesRoot, levelDirectory(depth)), { recursive: true });
  }
}

function cleanGeneratedFiles() {
  const defaultPath = path.join(root, "default.asp");
  if (fs.existsSync(defaultPath)) {
    fs.rmSync(defaultPath);
  }
  if (fs.existsSync(includesRoot)) {
    fs.rmSync(includesRoot, { recursive: true, force: true });
  }
}

function levelDirectory(depth) {
  return `level-${String(depth).padStart(2, "0")}`;
}

function nodeFileName(pathParts) {
  return `node-${pathParts.map((part) => String(part).padStart(2, "0")).join("-")}.inc`;
}

function childRelativePath(depth, childPathParts) {
  const fileName = nodeFileName(childPathParts);
  if (depth === 0) {
    return path.posix.join("includes", levelDirectory(1), fileName);
  }
  return path.posix.join("..", levelDirectory(depth + 1), fileName);
}

function nodeRelativePath(pathParts) {
  return path.join("includes", levelDirectory(pathParts.length), nodeFileName(pathParts));
}

function generatedHeader(file) {
  return [
    `<%@ Language="VBScript" %>`,
    `<!-- Generated by samples/classic-asp-include-tree-benchmark/generate.mjs: ${file} -->`,
  ];
}

function pushIncludeDirectives(lines, depth, pathParts) {
  if (depth >= includeDepth) {
    return;
  }
  for (let branch = 1; branch <= branchFactor; branch += 1) {
    lines.push(`<!-- #include file="${childRelativePath(depth, [...pathParts, branch])}" -->`);
  }
}

function pushBenchmarkBlock(lines, depth, pathParts, block) {
  const nodeId = pathParts.length === 0 ? "root" : pathParts.join("_");
  const padded = String(block).padStart(4, "0");
  const selector = `tree-${nodeId.replaceAll("_", "-")}-${padded}`;
  lines.push(`<!-- include tree node ${nodeId} block ${padded} -->`);
  lines.push(`<section class="include-tree-node" data-depth="${depth}" data-node="${nodeId}">`);
  lines.push(`  <h2>Include tree node ${nodeId}</h2>`);
  lines.push(`<%`);
  lines.push(`Dim includeTreeValue${padded}`);
  if (implicitGlobalMode) {
    lines.push(`implicitSharedValue = "${nodeId}-${padded}"`);
  }
  lines.push(`includeTreeValue${padded} = "${nodeId}-${padded}"`);
  lines.push(`If Len(includeTreeValue${padded}) > 0 Then`);
  lines.push(
    `    Response.Write "<span class=""tree-value"">" & Server.HTMLEncode(includeTreeValue${padded}) & "</span>"`,
  );
  lines.push(`End If`);
  lines.push(`%>`);
  lines.push(`  <p>Static include tree benchmark markup for ${nodeId}.</p>`);
  lines.push(`  <span class="${selector}" data-depth="<%= includeTreeDepth %>"></span>`);
  lines.push(`</section>`);
  lines.push(``);
}

function pushNodeEmbeddedAssets(lines, depth, pathParts) {
  const nodeId = pathParts.length === 0 ? "root" : pathParts.join("_");
  const className = `tree-node-${nodeId.replaceAll("_", "-")}`;
  lines.push(`<style>`);
  lines.push(`  .include-tree-benchmark[data-node="${nodeId}"] .${className} {`);
  lines.push(`    color: hsl(${(depth * 61 + pathParts.length) % 360} 60% 30%);`);
  lines.push(`    padding-left: ${depth + pathParts.length}px;`);
  lines.push(`  }`);
  lines.push(
    `  .include-tree-benchmark[data-node="${nodeId}"] .${className}::after { content: "${nodeId}"; }`,
  );
  lines.push(`</style>`);
  lines.push(`<script>`);
  lines.push(`  (function () {`);
  lines.push(`    const key = "${nodeId}";`);
  lines.push(
    `    const store = (window.aspLspIncludeTreeBenchmark = window.aspLspIncludeTreeBenchmark || {});`,
  );
  lines.push(
    `    store[key] = { depth: ${depth}, pathLength: ${pathParts.length}, children: ${branchFactor} };`,
  );
  lines.push(`    document.documentElement.dataset.aspLspIncludeTreeLastNode = key;`);
  lines.push(`  })();`);
  lines.push(`</script>`);
  lines.push(`<span class="${className}" data-depth="<%= includeTreeDepth %>"></span>`);
}

function buildNodeFile(relativePath, depth, pathParts) {
  const lines = generatedHeader(relativePath);
  pushIncludeDirectives(lines, depth, pathParts);
  lines.push(`<%`);
  lines.push(`Dim includeTreeDepth`);
  lines.push(`includeTreeDepth = ${depth}`);
  if (implicitGlobalMode) {
    lines.push(`implicitSharedValue = "${pathParts.length === 0 ? "root" : pathParts.join("_")}"`);
  }
  lines.push(`%>`);
  lines.push(
    `<div class="include-tree-benchmark" data-depth="${depth}" data-node="${pathParts.length === 0 ? "root" : pathParts.join("_")}">`,
  );
  pushNodeEmbeddedAssets(lines, depth, pathParts);

  let block = 1;
  while (lines.length + 15 < targetLines) {
    pushBenchmarkBlock(lines, depth, pathParts, block);
    block += 1;
  }

  lines.push(`</div>`);
  while (lines.length < targetLines) {
    lines.push(
      `<!-- include tree filler depth ${depth} line ${String(lines.length + 1).padStart(5, "0")} -->`,
    );
  }
  return `${lines.join("\n")}\n`;
}

function writeFile(relativePath, content) {
  fs.mkdirSync(path.dirname(path.join(root, relativePath)), { recursive: true });
  fs.writeFileSync(path.join(root, relativePath), content);
}

function writeNode(depth, pathParts) {
  const relativePath = depth === 0 ? "default.asp" : nodeRelativePath(pathParts);
  writeFile(relativePath, buildNodeFile(relativePath, depth, pathParts));
  if (depth >= includeDepth) {
    return;
  }
  for (let branch = 1; branch <= branchFactor; branch += 1) {
    writeNode(depth + 1, [...pathParts, branch]);
  }
}

cleanGeneratedFiles();
ensureDirectories();
writeNode(0, []);
